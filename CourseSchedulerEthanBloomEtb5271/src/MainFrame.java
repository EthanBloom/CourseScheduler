    private void dropStudentConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentConfirmButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student = (StudentEntry) dropStudentComboBox.getSelectedItem();
        String firstName = student.getFirstName();
        String lastName = student.getLastName();
        String studentID = student.getStudentID();

        dropStudentMessageArea.setText(lastName + ", " + firstName + " " + studentID + " has been dropped from the list of students.\n\n");
        
        ArrayList<String> semestersList = SemesterQueries.getSemesterList();
        for(String semester : semestersList)
        {
            String droppedCourses = "";
            String replacedCourses = "";
            ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            for(ScheduleEntry entry : studentSchedule)
            {
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, entry.getCourseCode());
                droppedCourses += lastName + ", " + firstName + " " + studentID + (entry.getStatus().equals("w") ? " has been dropped from the waitlist for ": " has been dropped from ") + entry.getCourseCode() + " .\n";
                
                ArrayList<ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByClass(semester, entry.getCourseCode());
                if(!waitlist.isEmpty())
                {
                    ScheduleEntry firstEntry = waitlist.get(0);
                    ScheduleQueries.updateScheduleEntry(firstEntry);
                    StudentEntry scheduledStudent = StudentQueries.getStudent(firstEntry.getStudentID());
                    replacedCourses += scheduledStudent.getLastName() + ", " + scheduledStudent.getFirstName() + " " + scheduledStudent.getStudentID() + " has been scheduled into " + entry.getCourseCode() + " .\n";
                }
            }
            if(!droppedCourses.isEmpty() || !replacedCourses.isEmpty())
            {
                dropStudentMessageArea.append("For Semester: " + semester + " \n");
                dropStudentMessageArea.append(droppedCourses + "\n");
                dropStudentMessageArea.append(replacedCourses);
            }
        }
        
        StudentQueries.dropStudent(studentID);
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_dropStudentConfirmButtonActionPerformed

