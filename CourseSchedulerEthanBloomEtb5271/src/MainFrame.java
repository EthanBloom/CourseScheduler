    private void scheduleClassesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassesSubmitButtonActionPerformed
        // TODO add your handling code here:
        String aClass = scheduleClassesSelectClassComboBox.getSelectedItem().toString();
        StudentEntry student = (StudentEntry) scheduleClassesSelectStudentComboBox.getSelectedItem();
        String status = "w";
        
        if (ClassQueries.getClassSeats(currentSemester, aClass) <= ScheduleQueries.getScheduledStudentCount(currentSemester, aClass))
        {
            scheduleClassesStatusLabel.setText(student.getFirstName() + " " + student.getLastName() + " has been waitlisted for " + aClass + " .");
        }
        else
        {
            status = "s";
            scheduleClassesStatusLabel.setText(student.getFirstName() + " " + student.getLastName() + " has been scheduled for " + aClass + " .");
        }
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        ScheduleQueries.addScheduleEntry(new ScheduleEntry(currentSemester, aClass, student.getStudentID(), status, currentTimestamp));
    }//GEN-LAST:event_scheduleClassesSubmitButtonActionPerformed

